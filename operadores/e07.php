<?php
// operadores incrementais e decrementais

$a =10;

echo $a++; //mostra o valor e soma 1 na variavel 

echo "<br>";

echo $a;

echo "<br>";
echo "<br>";

echo ++$a; // soma 1 na variavel e mostra o valor

echo "<br>";
echo "<br>";
echo "<br>";


echo $a--; //mostra o valor e subtrai 1 na variavel  
echo "<br>";
echo $a;
echo "<br>";
echo --$a; //subtrai 1 na variavel e mostra o valor

?>